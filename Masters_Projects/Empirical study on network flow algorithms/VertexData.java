public class VertexData {

    private int height;
    private double excess;


    public VertexData() {
        this.height = 0;
        this.excess = 0.0;
    }

    public int getHeight() {

        return this.height;
    }

    public void setHeight(int height) {

        this.height = height;
    }

    public double getExcess() {

        return this.excess;
    }

    public void setExcess(double excess) {

        this.excess = excess;
    }
}